package ExamplePrograms1;

public class Package {

	protected void show() {
		System.out.println("This class in another package and protected modifier");
	}
	public void show1() {
		System.out.println("This class in another package and public modifier");
	}
	private void show2() {
		System.out.println("This class in another package and private modifier");
	}
	public static void show3() {
		System.out.println("This class in ExamplePrograms1 Package and class Name Package");
	}
}
